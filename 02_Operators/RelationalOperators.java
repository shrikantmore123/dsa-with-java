public class RelationalOperators {
    public static void main(String args[]) {
        int A = 10;
        int B = 5;
        System.out.println("A: " + A);
        System.out.println("B: " + B);
        System.out.println("A == B: " + (A == B));
        System.out.println("A != B: " + (A != B));
        System.out.println("A > B: " + (A > B));
        System.out.println("A < B: " + (A < B));
        System.out.println("A >= B: " + (A >= B));
        System.out.println("A <= B: " + (A <= B));
    }
}